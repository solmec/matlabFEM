function [ dN ] = gradL4New( xi )
  r  = xi(1,:)';
  s  = xi(2,:)';
  np = size( xi, 2 );
  dN = zeros( 4, 2, np );
  %dN = [ -(1-s)/4 -(1-r)/4;  (1-s)/4 -(1+r)/4;   (1+s)/4 (1+r)/4;  -(1+s)/4 (1-r)/4 ];
  dN(1,1,:) = -(1-s)/4; dN(2,1,:) = (1-s)/4;; dN(3,1,:) = (1+s)/4; dN(4,1,:) = -(1+s)/4;
  dN(1,2,:) = -(1-r)/4; dN(2,2,:) = -(1+r)/4; dN(3,2,:) = (1+r)/4; dN(4,2,:) =  (1-r)/4 ;
end


